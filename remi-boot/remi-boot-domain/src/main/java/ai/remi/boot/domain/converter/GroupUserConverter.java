package ai.remi.boot.domain.converter;


import ai.remi.boot.domain.dto.post.GroupUserPostDTO;
import ai.remi.boot.domain.dto.put.GroupUserPutDTO;
import ai.remi.boot.domain.entity.GroupUser;
import ai.remi.boot.domain.query.GroupUserQuery;
import ai.remi.boot.domain.vo.GroupUserVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 组织用户 GroupUserConverter转换接口
 */
@Mapper
public interface GroupUserConverter {

    GroupUserConverter INSTANT = Mappers.getMapper(GroupUserConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    GroupUser postDtoToEntity(GroupUserPostDTO groupUserDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    GroupUser putDtoToEntity(GroupUserPutDTO groupUserDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    GroupUser queryToEntity(GroupUserQuery groupUserQuery);

    GroupUserVO entityToVO(GroupUser groupUser);

}
