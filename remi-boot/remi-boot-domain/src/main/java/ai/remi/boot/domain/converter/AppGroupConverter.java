package ai.remi.boot.domain.converter;


import ai.remi.boot.domain.dto.post.AppGroupPostDTO;
import ai.remi.boot.domain.dto.put.AppGroupPutDTO;
import ai.remi.boot.domain.entity.AppGroup;
import ai.remi.boot.domain.query.AppGroupQuery;
import ai.remi.boot.domain.vo.AppGroupVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 应用组织 AppGroupConverter转换接口
 */
@Mapper
public interface AppGroupConverter {

    AppGroupConverter INSTANT = Mappers.getMapper(AppGroupConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppGroup postDtoToEntity(AppGroupPostDTO appGroupDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppGroup putDtoToEntity(AppGroupPutDTO appGroupDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppGroup queryToEntity(AppGroupQuery appGroupQuery);

    AppGroupVO entityToVO(AppGroup appGroup);

}
