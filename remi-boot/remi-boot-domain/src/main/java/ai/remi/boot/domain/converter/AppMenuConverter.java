package ai.remi.boot.domain.converter;


import ai.remi.boot.domain.dto.post.AppMenuPostDTO;
import ai.remi.boot.domain.dto.put.AppMenuPutDTO;
import ai.remi.boot.domain.entity.AppMenu;
import ai.remi.boot.domain.query.AppMenuQuery;
import ai.remi.boot.domain.vo.AppMenuVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 应用菜单表 AppMenuConverter转换接口
 */
@Mapper
public interface AppMenuConverter {

    AppMenuConverter INSTANT = Mappers.getMapper(AppMenuConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppMenu postDtoToEntity(AppMenuPostDTO appMenuDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppMenu putDtoToEntity(AppMenuPutDTO appMenuDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    AppMenu queryToEntity(AppMenuQuery appMenuQuery);

    AppMenuVO entityToVO(AppMenu appMenu);

}
